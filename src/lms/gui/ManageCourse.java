    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String course_id = courseidTxt.getText();
        String course_name = coursenameTxt.getText();
        String credit_hours = credithourTxt.getText();
        String status = (String) statusCombo.getSelectedItem();
        String fee = feeTxt.getText();

        LocalDate today = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        String formattedDate = today.format(formatter);
        System.out.println(formattedDate);

        if (course_name.equals("")) {
            JOptionPane.showMessageDialog(null, "Course Name field is required.");
        } else if (credit_hours.equals("")) {
            JOptionPane.showMessageDialog(null, "Credit Hour/s field is required.");
        } else if (fee.equals("")) {
            JOptionPane.showMessageDialog(null, "Fee field is required.");
        } else {
            try {
                Connection con = ProvideConnection.getConnection();
                PreparedStatement ps = con.prepareStatement("UPDATE course SET course_name = ?, credit_hours = ?, status = ?, fee = ?, modified_date = ? WHERE course_id = ?;");
                ps.setInt(6, Integer.parseInt(course_id));
                ps.setString(1, course_name);
                ps.setInt(2, Integer.parseInt(credit_hours));
                ps.setString(3, status);
                ps.setFloat(4, Float.parseFloat(fee));
                ps.setString(5, formattedDate);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Course Updated Successfully.");
                setVisible(false);
                new ManageCourse().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_updateBtnActionPerformed

