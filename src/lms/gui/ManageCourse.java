    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) courseTbl.getModel();
        try {
            Connection con = ProvideConnection.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM course");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt("course_id"), rs.getString("course_name"), rs.getInt("credit_hours"), rs.getString("status"), rs.getFloat("fee"), rs.getString("created_date"), rs.getString("modified_date")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formComponentShown

    private void courseTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_courseTblMouseClicked
        // TODO add your handling code here:
        // TODO add your handling code here:
        int index = courseTbl.getSelectedRow();
        TableModel model = courseTbl.getModel();
        String courseId = model.getValueAt(index, 0).toString();
        try {
            Connection con = ProvideConnection.getConnection();
            PreparedStatement ps = con.prepareStatement("SELECT * FROM course where course_id=?");
            ps.setString(1, courseId);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                courseidTxt.setText("" + rs.getInt("course_id"));
                coursenameTxt.setText(rs.getString("course_name"));
                credithourTxt.setText("" + rs.getInt("credit_hours"));
                String status = rs.getString("status");
                if (status.endsWith("Active")) {
                    statusCombo.removeAllItems();
                    statusCombo.addItem("Active");
                    statusCombo.addItem("Inactive");
                } else {
                    statusCombo.removeAllItems();
                    statusCombo.addItem("Inactive");
                    statusCombo.addItem("Active");
                }
                feeTxt.setText("" + rs.getFloat("fee"));
            }
            courseidTxt.setEditable(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_courseTblMouseClicked

