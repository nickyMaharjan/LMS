    private void addBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtn2ActionPerformed
        //        // TODO add your handling code here:
        String student_id = studentidTxt.getText();
        String first_name = firstnameTxt.getText();
        String last_name = lastnameTxt.getText();
        String address = addressTxt.getText();
        String phone_number = phoneTxt.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date_of_birth = dobDate.getDate();
        Date enrolled_Date = enrolledDate.getDate();
        String course = (String) courseCombo.getSelectedItem();
        String dob = "";
        String enrolldate = "";
        if (date_of_birth != null) {
            dob = dFormat.format(date_of_birth.getDate());
        }
        if (enrolled_Date != null) {
            enrolldate = dFormat.format(enrolled_Date.getDate());
        }

        if (student_id.equals("")) {
            JOptionPane.showMessageDialog(null, "Student Id field is required.");
        } else if (first_name.equals("")) {
            JOptionPane.showMessageDialog(null, "First Name field is required.");
        } else if (last_name.equals("")) {
            JOptionPane.showMessageDialog(null, "Last Name field is required.");
        } else if (address.equals("")) {
            JOptionPane.showMessageDialog(null, "Address field is required.");
        } else if (phone_number.equals("")) {
            JOptionPane.showMessageDialog(null, "Phone number field is required.");
        } else if (!phone_number.matches(mobileNumberPattern) || phone_number.length() != 10) {
            JOptionPane.showMessageDialog(null, "Phone number filed is invalid.");
        } else if (dob.equals("")) {
            JOptionPane.showMessageDialog(null, "Date of birth field is required.");
        } else if (enrolldate.equals("")) {
            JOptionPane.showMessageDialog(null, "Enrolled Date field is required.");
        } else {
            try {
                Connection con = ProvideConnection.getConnection();
                PreparedStatement ps = con.prepareStatement("INSERT INTO student(student_id, first_name, last_name, date_of_birth, phone_number, address, course, enrolled_date) VALUES(?,?,?,?,?,?,?,?)");
                ps.setInt(1, Integer.parseInt(student_id));
                ps.setString(2, first_name);
                ps.setString(3, last_name);
                ps.setDate(4, new java.sql.Date(date_of_birth.getTime()));
                ps.setString(5, phone_number);
                ps.setString(6, address);
                ps.setString(7, course);
                ps.setDate(8, new java.sql.Date(enrolled_Date.getTime()));
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Student Added Successfully.");
                setVisible(false);
                new ManageStudent().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_addBtn2ActionPerformed

