            while (resultSet.next()) {
                courseCombo.addItem(resultSet.getString("course_name"));
            }
            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt("student_id"), rs.getString("first_name"), rs.getString("last_name"), rs.getString("address"), rs.getString("phone_number"), rs.getDate("date_of_birth"), rs.getString("course"), rs.getDate("enrolled_date")});

            }
