    private void studentTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTblMouseClicked
        // TODO add your handling code here:
        int index = studentTbl.getSelectedRow();
        TableModel model = studentTbl.getModel();
        String studentId = model.getValueAt(index, 0).toString();
        try {
            Connection con = ProvideConnection.getConnection();
            PreparedStatement ps = con.prepareStatement("SELECT * FROM student where student_id=?");
            ps.setString(1, studentId);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                studentidTxt.setText("" + rs.getInt("student_id"));
                firstnameTxt.setText(rs.getString("first_name"));
                lastnameTxt.setText(rs.getString("last_name"));
                addressTxt.setText(rs.getString("address"));
                phoneTxt.setText(rs.getString("phone_number"));
                addressTxt.setText(rs.getString("address"));
                dobDate.setDate(rs.getDate("date_of_birth"));
                courseCombo.setSelectedItem(rs.getString("course"));
                enrolledDate.setDate(rs.getDate("enrolled_date"));
            }
            studentidTxt.setEditable(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_studentTblMouseClicked

