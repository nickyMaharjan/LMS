    public void studentSearch(String search) {
        DefaultTableModel model = (DefaultTableModel) studentTbl.getModel();
        model.setRowCount(0);
        try {
            Connection con = ProvideConnection.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM student WHERE first_name LIKE '" + search + "%' OR student_id LIKE '" + search + "%'");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt("student_id"), rs.getString("first_name"), rs.getString("last_name"), rs.getString("address"), rs.getString("phone_number"), rs.getDate("date_of_birth"), rs.getString("course"), rs.getDate("enrolled_date")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

